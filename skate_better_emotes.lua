local function TwitchEmotesAnimator_UpdateEmoteInFontString(fontstring, widthOverride, heightOverride)
    local txt = fontstring:GetText();
    if (txt ~= nil) then
        if issecretvalue(txt) then
            return
        end
        for _, pattern in ipairs(patterns) do
            for emoteTextureString in string.gmatch(txt, pattern) do
                local imagepath = string.match(emoteTextureString, "|T(Interface\\AddOns\\TwitchEmotes\\Emotes.-%.tga).-|t")
                if not imagepath then
                    imagepath = string.match(emoteTextureString, "|T(Interface\\AddOns\\SkateBetterEmotes\\Emotes.-%.tga).-|t")
                end
                if not imagepath then
                    print('No image path found in SkateBetterEmotes')
                    return
                end

                local animdata = TwitchEmotes_animation_metadata[imagepath];
                -- print(animdata)
                if (animdata ~= nil) then
                    local framenum = TwitchEmotes_GetCurrentFrameNum(animdata);
                    local nTxt;
                    if(widthOverride ~= nil or heightOverride ~= nil) then
                        nTxt = string.gsub(txt, escpattern(emoteTextureString),
                                            TwitchEmotes_BuildEmoteFrameStringWithDimensions(
                                            imagepath, animdata, framenum, widthOverride, heightOverride))
                    else
                        nTxt = string.gsub(txt, escpattern(emoteTextureString),
                                        TwitchEmotes_BuildEmoteFrameString(
                                            imagepath, animdata, framenum))
                    end

                    -- If we're updating a chat message we need to alter the messageInfo as wel
                    if (fontstring.messageInfo ~= nil) then
                        fontstring.messageInfo.message = nTxt
                    end
                    fontstring:SetText(nTxt);
                    txt = nTxt;
                end
            end
        end
    end
end

local function Emoticons_SetAutoComplete(state)
    
    Emoticons_Settings["ENABLE_AUTOCOMPLETE"] = state

    if Emoticons_Settings["ENABLE_AUTOCOMPLETE"] and not autocompleteInited then
        AllTwitchEmoteNames = {};

        local i = 1;
        for k, v in pairs(TwitchEmotes_defaultpack) do
            --Some values in emoticons don't have a corresponding key in TwitchEmotes_defaultpack
            --we need to filter these out because we don't have an emote to show for these
            -- if TwitchEmotes_defaultpack[v] ~= nil then
                local excluded = false;
                for j=1, #TwitchEmotes_ExcludedSuggestions do
                    if k == TwitchEmotes_ExcludedSuggestions[j] then
                        excluded = true;
                        break;
                    end
                end

                if excluded == false then
                    AllTwitchEmoteNames[i] = k;
                    i = i + 1;
                end
            -- end
        end

        --Sort the list alphabetically
        table.sort(AllTwitchEmoteNames)

        for _, frameName in pairs(CHAT_FRAMES) do
            local frame = _G[frameName]

            local editbox = frame.editBox;
            local suggestionList = AllTwitchEmoteNames;
            local maxButtonCount = 20;

            local autocompletesettings = {
                perWord = true,
                activationChar = ':',
                closingChar = ':',
                minChars = 2,
                fuzzyMatch = true,
                onSuggestionApplied = function(suggestion)
                    UpdateEmoteStats(suggestion, true, false, false);
                end,
                renderSuggestionFN = Emoticons_RenderSuggestionFN,
                suggestionBiasFN = function(suggestion, text)
                    --Bias the sorting function towards the most autocompleted emotes
                    if TwitchEmoteStatistics[suggestion] ~= nil then
                        return TwitchEmoteStatistics[suggestion][1] * 5
                    end
                    return 0;
                end,
                interceptOnEnterPressed = true,
                addSpace = true,
                useTabToConfirm = Emoticons_Settings["AUTOCOMPLETE_CONFIRM_WITH_TAB"],
                useArrowButtons = true,
            }

            SetupAutoComplete(editbox, suggestionList, maxButtonCount, autocompletesettings);
            
        end
    
        autocompleteInited = true;
    end

end


local function CreateEmoteBrowser()
    if SBE_EmoteBrowserFrame then
        SBE_EmoteBrowserFrame:Show()
        return
    end

    local EMOTE_SIZE = 64
    local CELL_WIDTH = 90
    local CELL_HEIGHT = 90
    local COLUMNS = 5
    local WINDOW_WIDTH = COLUMNS * CELL_WIDTH + 50
    local WINDOW_HEIGHT = 500

    local mainFrame = CreateFrame("Frame", "SBE_EmoteBrowserFrame", UIParent, "BasicFrameTemplateWithInset")
    mainFrame:SetSize(WINDOW_WIDTH, WINDOW_HEIGHT)
    mainFrame:SetPoint("CENTER")
    mainFrame:SetMovable(true)
    mainFrame:EnableMouse(true)
    mainFrame:RegisterForDrag("LeftButton")
    mainFrame:SetScript("OnDragStart", mainFrame.StartMoving)
    mainFrame:SetScript("OnDragStop", mainFrame.StopMovingOrSizing)
    mainFrame:SetFrameStrata("DIALOG")
    mainFrame.TitleText:SetText("SkateBetter Emotes")

    local scrollFrame = CreateFrame("ScrollFrame", nil, mainFrame, "UIPanelScrollFrameTemplate")
    scrollFrame:SetPoint("TOPLEFT", mainFrame, "TOPLEFT", 12, -32)
    scrollFrame:SetPoint("BOTTOMRIGHT", mainFrame, "BOTTOMRIGHT", -30, 10)

    local content = CreateFrame("Frame", nil, scrollFrame)
    content:SetWidth(COLUMNS * CELL_WIDTH)
    scrollFrame:SetScrollChild(content)

    -- Build a sorted, deduplicated emote list
    local emoteList = {}
    local seen = {}
    for name, _ in pairs(defaultPack) do
        if not seen[name] then
            seen[name] = true
            table.insert(emoteList, name)
        end
    end
    table.sort(emoteList)

    local animatedTextures = {}

    local col = 0
    local row = 0
    for _, name in ipairs(emoteList) do
        local dirString = defaultPack[name]
        local texPath = string.match(dirString, "^(.-%.tga)")

        local cellFrame = CreateFrame("Frame", nil, content)
        cellFrame:SetSize(CELL_WIDTH, CELL_HEIGHT)
        cellFrame:SetPoint("TOPLEFT", content, "TOPLEFT",
            col * CELL_WIDTH + 5,
            -(row * CELL_HEIGHT + 5))

        local tex = cellFrame:CreateTexture(nil, "ARTWORK")
        tex:SetSize(EMOTE_SIZE, EMOTE_SIZE)
        tex:SetPoint("TOP", cellFrame, "TOP", 0, -5)
        tex:SetTexture(texPath)

        local animdata = animatedPack[texPath]
        if animdata then
            -- Start on frame 0
            local frameH = animdata.frameHeight / animdata.imageHeight
            tex:SetTexCoord(0, 1, 0, frameH)
            table.insert(animatedTextures, {tex = tex, animdata = animdata})
        end

        local label = cellFrame:CreateFontString(nil, "OVERLAY", "GameFontHighlightSmall")
        label:SetPoint("BOTTOM", cellFrame, "BOTTOM", 0, 5)
        label:SetWidth(CELL_WIDTH - 4)
        label:SetText(name)
        label:SetJustifyH("CENTER")
        label:SetWordWrap(false)

        col = col + 1
        if col >= COLUMNS then
            col = 0
            row = row + 1
        end
    end

    content:SetHeight(math.ceil(#emoteList / COLUMNS) * CELL_HEIGHT + 10)

    -- Drive animations via OnUpdate
    mainFrame:SetScript("OnUpdate", function()
        local now = GetTime()
        for _, entry in ipairs(animatedTextures) do
            local d = entry.animdata
            local frameIndex = math.floor(now * d.framerate) % d.nFrames
            local frameH = d.frameHeight / d.imageHeight
            local top = frameIndex * frameH
            entry.tex:SetTexCoord(0, 1, top, top + frameH)
        end
    end)
end

