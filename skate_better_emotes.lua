local function AppendNewEmotes()
    if not TwitchEmotes_defaultpack then
        return
    end
    if not TwitchEmotes_animation_metadata then
        return
    end
    if not TwitchEmotes_emoticons then
        return
    end
    local function debugPrint(...)
        debugMode = false -- Change this if you want to debug AppendNewEmotes
        if debugMode == true then
            print(...)
        end
    end

    for emote, directory in pairs(defaultPack) do
        TwitchEmotes_defaultpack[emote] = directory
        debugPrint("DefaultPack - Appended emote:", emote, "Directory:", directory)
    end
    for emote, metadata in pairs(animatedPack) do
        TwitchEmotes_animation_metadata[emote] = metadata
        debugPrint("AnimatedPack - Appended emote:", emote, "Metadata:", metadata)
    end
    for emote, name in pairs(emoticons) do
        TwitchEmotes_emoticons[emote] = name
        debugPrint("Emoticons - Appended emote:", emote, "Name:", name)
    end
    -- Add custom minimap dropdown
    TwitchEmotes_dropdown_options[#TwitchEmotes_dropdown_options + 1] = dropdown
    -- Remove LMAO because i like the text better than the emoji lol
    TwitchEmotes_defaultpack["LMAO"] = nil
end

local function escpattern(x)
    x = string.gsub(x, '%%', '%%%%')
    x = string.gsub(x, '^%^', '%%^')
    x = string.gsub(x, '%$$', '%%$')
    x = string.gsub(x, '%(', '%%(');
    x = string.gsub(x, '%)', '%%)')
    x = string.gsub(x, '%.', '%%.')
    x = string.gsub(x, '%[', '%%[')
    x = string.gsub(x, '%]', '%%]')
    x = string.gsub(x, '%*', '%%*')
    x = string.gsub(x, '%+', '%%+')
    x = string.gsub(x, '%-', '%%-')
    x = string.gsub(x, '%?', '%%?')
    return x
end

local patterns = {
    "|TInterface\\AddOns\\TwitchEmotes\\Emotes.-|t",
    "|TInterface\\AddOns\\SkateBetterEmotes\\Emotes.-|t"
}

local function TwitchEmotesAnimator_UpdateEmoteInFontString(fontstring, widthOverride, heightOverride)
    local txt = fontstring:GetText();
    if (txt ~= nil) then
        if issecretvalue(txt) then
            return
        end
        for _, pattern in ipairs(patterns) do
            for emoteTextureString in string.gmatch(txt, pattern) do
                local imagepath = string.match(emoteTextureString, "|T(Interface\\AddOns\\TwitchEmotes\\Emotes.-%.tga).-|t")
                if not imagepath then
                    imagepath = string.match(emoteTextureString, "|T(Interface\\AddOns\\SkateBetterEmotes\\Emotes.-%.tga).-|t")
                end
                if not imagepath then
                    print('No image path found in SkateBetterEmotes')
                    return
                end

                local animdata = TwitchEmotes_animation_metadata[imagepath];
                -- print(animdata)
                if (animdata ~= nil) then
                    local framenum = TwitchEmotes_GetCurrentFrameNum(animdata);
                    local nTxt;
                    if(widthOverride ~= nil or heightOverride ~= nil) then
                        nTxt = string.gsub(txt, escpattern(emoteTextureString),
                                            TwitchEmotes_BuildEmoteFrameStringWithDimensions(
                                            imagepath, animdata, framenum, widthOverride, heightOverride))
                    else
                        nTxt = string.gsub(txt, escpattern(emoteTextureString),
                                        TwitchEmotes_BuildEmoteFrameString(
                                            imagepath, animdata, framenum))
                    end

                    -- If we're updating a chat message we need to alter the messageInfo as wel
                    if (fontstring.messageInfo ~= nil) then
                        fontstring.messageInfo.message = nTxt
                    end
                    fontstring:SetText(nTxt);
                    txt = nTxt;
                end
            end
        end
    end
end

local function Emoticons_SetAutoComplete(state)
    
    Emoticons_Settings["ENABLE_AUTOCOMPLETE"] = state

    if Emoticons_Settings["ENABLE_AUTOCOMPLETE"] and not autocompleteInited then
        AllTwitchEmoteNames = {};

        local i = 1;
        for k, v in pairs(TwitchEmotes_defaultpack) do
            --Some values in emoticons don't have a corresponding key in TwitchEmotes_defaultpack
            --we need to filter these out because we don't have an emote to show for these
            -- if TwitchEmotes_defaultpack[v] ~= nil then
                local excluded = false;
                for j=1, #TwitchEmotes_ExcludedSuggestions do
                    if k == TwitchEmotes_ExcludedSuggestions[j] then
                        excluded = true;
                        break;
                    end
                end

                if excluded == false then
                    AllTwitchEmoteNames[i] = k;
                    i = i + 1;
                end
            -- end
        end

        --Sort the list alphabetically
        table.sort(AllTwitchEmoteNames)

        for _, frameName in pairs(CHAT_FRAMES) do
            local frame = _G[frameName]

            local editbox = frame.editBox;
            local suggestionList = AllTwitchEmoteNames;
            local maxButtonCount = 20;

            local autocompletesettings = {
                perWord = true,
                activationChar = ':',
                closingChar = ':',
                minChars = 2,
                fuzzyMatch = true,
                onSuggestionApplied = function(suggestion)
                    UpdateEmoteStats(suggestion, true, false, false);
                end,
                renderSuggestionFN = Emoticons_RenderSuggestionFN,
                suggestionBiasFN = function(suggestion, text)
                    --Bias the sorting function towards the most autocompleted emotes
                    if TwitchEmoteStatistics[suggestion] ~= nil then
                        return TwitchEmoteStatistics[suggestion][1] * 5
                    end
                    return 0;
                end,
                interceptOnEnterPressed = true,
                addSpace = true,
                useTabToConfirm = Emoticons_Settings["AUTOCOMPLETE_CONFIRM_WITH_TAB"],
                useArrowButtons = true,
            }

            SetupAutoComplete(editbox, suggestionList, maxButtonCount, autocompletesettings);
            
        end
    
        autocompleteInited = true;
    end

end


