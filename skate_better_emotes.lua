local function AppendNewEmotes()
    if not TwitchEmotes_defaultpack then
        return
    end
    if not TwitchEmotes_animation_metadata then
        return
    end
    if not TwitchEmotes_emoticons then
        return
    end
    local function debugPrint(...)
        debugMode = false -- Change this if you want to debug AppendNewEmotes
        if debugMode == true then
            print(...)
        end
    end

    for emote, directory in pairs(defaultPack) do
        TwitchEmotes_defaultpack[emote] = directory
        debugPrint("DefaultPack - Appended emote:", emote, "Directory:", directory)
    end
    for emote, metadata in pairs(animatedPack) do
        TwitchEmotes_animation_metadata[emote] = metadata
        debugPrint("AnimatedPack - Appended emote:", emote, "Metadata:", metadata)
    end
    for emote, name in pairs(emoticons) do
        TwitchEmotes_emoticons[emote] = name
        debugPrint("Emoticons - Appended emote:", emote, "Name:", name)
    end
    -- Add custom minimap dropdown
    local dropdown_count = #TwitchEmotes_dropdown_options
    TwitchEmotes_dropdown_options[dropdown_count + 1] = dropdown_1
    TwitchEmotes_dropdown_options[dropdown_count + 2] = dropdown_2
    -- Remove LMAO because i like the text better than the emoji lol
    TwitchEmotes_defaultpack["LMAO"] = nil
end

local function escpattern(x)
    x = string.gsub(x, '%%', '%%%%')
    x = string.gsub(x, '^%^', '%%^')
    x = string.gsub(x, '%$$', '%%$')
    x = string.gsub(x, '%(', '%%(');
    x = string.gsub(x, '%)', '%%)')
    x = string.gsub(x, '%.', '%%.')
    x = string.gsub(x, '%[', '%%[')
    x = string.gsub(x, '%]', '%%]')
    x = string.gsub(x, '%*', '%%*')
    x = string.gsub(x, '%+', '%%+')
    x = string.gsub(x, '%-', '%%-')
    x = string.gsub(x, '%?', '%%?')
    return x
end

